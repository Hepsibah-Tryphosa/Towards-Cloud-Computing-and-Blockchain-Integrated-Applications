public class CustomerDetails {

    private String loginuser;
    private String insuranceuser;
    private String mobile;
    private String email;
    private String locality;
    private String address;
    private String insurtype;
    private String insurancepremium;
    private String bnakname;

    public String getLoginuser() {
        return loginuser;
    }

    public void setLoginuser(String loginuser) {
        this.loginuser = loginuser;
    }

    public String getInsuranceuser() {
        return insuranceuser;
    }

    public void setInsuranceuser(String insuranceuser) {
        this.insuranceuser = insuranceuser;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getLocality() {
        return locality;
    }

    public void setLocality(String locality) {
        this.locality = locality;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getInsurtype() {
        return insurtype;
    }

    public void setInsurtype(String insurtype) {
        this.insurtype = insurtype;
    }

    public String getInsurancepremium() {
        return insurancepremium;
    }

    public void setInsurancepremium(String insurancepremium) {
        this.insurancepremium = insurancepremium;
    }

    public String getBnakname() {
        return bnakname;
    }

    public void setBnakname(String bnakname) {
        this.bnakname = bnakname;
    }
    
    

}
